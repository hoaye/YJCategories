#pragma mark - 字符加密
- (NSString *)yj_md5String {
    
    const char *str = self.UTF8String;
    uint8_t buffer[CC_MD5_DIGEST_LENGTH];
    
    CC_MD5(str, (CC_LONG)strlen(str), buffer);
    
    return [self yj_stringFromBytes:buffer length:CC_MD5_DIGEST_LENGTH];
}

